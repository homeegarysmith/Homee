          cy.get('[name="insured.firstName"]').type('Automation')
          cy.get('[name="insured.lastName"]').type('Testing')
          cy.get('[name="insured.phoneNumber"]').type('5135551234')
          cy.get('#address').type('9648 Conner Ct{enter}')
          cy.wait(1000)
          cy.get('#address').type(',{enter}')
          cy.get('#coverageConcerns').type('Automated notes entered in for Coverage concerns.')
          cy.get('#deductibleAmount').type('1000')
          cy.contains('Payment Breakdown').click()
          .type('Automated text entered for the Payment Breakdown field. This field does not have an id associated to it so we had to identify with the field title.')
          cy.get('[type="submit"]').click()
          //Confirm that new Claims is created
          cy.get('div.jsx-294498644').should('contain.text', 'Claim has been added successfully.')
