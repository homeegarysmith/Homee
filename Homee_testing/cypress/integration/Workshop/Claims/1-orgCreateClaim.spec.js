          cy.get('#claimNumber').type('AUD-')
               .type(claimID)
          cy.get('#referralType').type('General{enter}')
          cy.get('#dateOfLoss').click()
          cy.get('.CalendarDay__today').click()
          cy.get('#fnolDate').click()
          cy.get('.CalendarDay__today').click()
